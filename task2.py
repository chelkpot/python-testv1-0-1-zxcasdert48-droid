# Запрашиваем основание у пользователя
a = float(input("Введите основание треугольника (a): "))

# Запрашиваем высоту у пользователя
h = float(input("Введите высоту треугольника (h): "))

# Вычисляем площадь треугольника
S = 0.5 * a * h

# Выводим результат
print("Площадь треугольника:", S)
